#include "../philo.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	res;
	
	res = 0;
	i = 0;
	while (nptr[i])
	{
		if (nptr[i] <= '9' && nptr[i] >= '0')
			res = res * 10 + (nptr[i] - '0');
		else
			exit(0);
		i++;
	}
	return (res);
}
